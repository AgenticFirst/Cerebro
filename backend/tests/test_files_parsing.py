def test_parse_batch(parsing_client, tmp_path):
    docx_path = tmp_path / "a.docx"
    xlsx_path = tmp_path / "b.xlsx"
    bad_path = tmp_path / "missing.pdf"
    _write_docx(str(docx_path))
    _write_xlsx(str(xlsx_path))
    res = parsing_client.post(
        "/files/parse/batch",
        json={
            "files": [
                {"file_path": str(docx_path)},
                {"file_path": str(xlsx_path)},
                {"file_path": str(bad_path)},
            ]
        },
    )
    assert res.status_code == 200, res.text
    items = res.json()["items"]
    assert items[0]["ok"] is True
    assert items[1]["ok"] is True
    assert items[2]["ok"] is False
    assert "not found" in items[2]["error"]


def test_get_parsed_by_sha(parsing_client, tmp_path):
    src = tmp_path / "estructura.docx"
    _write_docx(str(src))
    parsed = parsing_client.post("/files/parse", json={"file_path": str(src)}).json()
    sha = parsed["sha256"]

    res = parsing_client.get(f"/files/parsed/{sha}")
    assert res.status_code == 200
    body = res.json()
    assert body is not None
    assert body["sha256"] == sha
    assert body["cached"] is True

    miss = parsing_client.get("/files/parsed/" + "0" * 64)
    assert miss.status_code == 200
    assert miss.json() is None


def test_from_path_registers_file_item_with_sniffed_mime(parsing_client, tmp_path):
    src = tmp_path / "estructura.docx"
    _write_docx(str(src))
    res = parsing_client.post(
        "/files/items/from-path",
        json={"file_path": str(src), "source": "chat-upload"},
    )
    assert res.status_code == 201, res.text
    body = res.json()
    assert body["sha256"] is not None and len(body["sha256"]) == 64
    assert body["mime"] == (
        "application/vnd.openxmlformats-officedocument.wordprocessingml.document"
    )
    assert body["storage_kind"] == "workspace"
    assert body["storage_path"] == str(src)
    assert body["source"] == "chat-upload"

    # Re-register same path → de-dupes by (sha, path, source).
    res2 = parsing_client.post(
        "/files/items/from-path",
        json={"file_path": str(src), "source": "chat-upload"},
    )
    assert res2.status_code == 201
    assert res2.json()["id"] == body["id"]


